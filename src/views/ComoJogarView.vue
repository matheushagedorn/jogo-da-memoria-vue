           <h1> Objetivo do jogo</h1>
            
            O objetivo do jogo da memória é encontrar pares de cartas correspondentes em um conjunto de cartas viradas para baixo. Quanto mais pares você encontrar, mais pontos você ganha.
                    
            Preparação:
                    
            Comece escolhendo um conjunto de cartas. Geralmente, as cartas são dispostas em uma grade, com todas as cartas viradas para baixo.
            Misture as cartas para garantir que elas estejam bem embaralhadas.
            
            Como jogar:
                    
            O jogador começa virando duas cartas.
            Se as duas cartas viradas correspondem (ou seja, têm a mesma imagem ou símbolo), o jogador mantém essas cartas e ganha um ponto. Essas cartas são removidas do jogo.
            Se as duas cartas não correspondem, o jogador deve virá-las de volta para baixo na mesma posição em que estavam.
            O jogo continua alternando entre os jogadores até que todas as cartas tenham sido combinadas em pares.
            O jogador com mais pares no final do jogo é o vencedor.
            
            Dicas para melhorar sua memória no jogo:
                    
            Concentre-se em lembrar onde estão as cartas que você já virou. Isso pode ajudá-lo a encontrar pares mais rapidamente.
            Procure padrões e cores nas cartas. Isso pode facilitar a identificação de pares.
            Jogue regularmente para praticar e melhorar sua memória.
            
            Variantes do jogo:
                    
            Variação do tempo: Defina um limite de tempo para encontrar todos os pares. Isso adiciona um elemento de pressão e emoção ao jogo.
            Variação das regras: Você pode adicionar regras extras, como permitir aos jogadores espiar algumas cartas antes de começar, ou permitir que eles troquem uma carta que já viraram por outra em qualquer momento do jogo.
